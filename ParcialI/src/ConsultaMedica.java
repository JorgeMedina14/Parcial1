public class ConsultaMedica implements Consulta{
    private String diagnosticopresentado;
    @Override
    public String agendarConsulta() {
        return agendarConsulta();
    }

    @Override
    public String realizarConsulta() {
        return realizarConsulta();
    }

    public String getDiagnosticopresentado() {
        return diagnosticopresentado;
    }

    public void setDiagnosticopresentado(String diagnosticopresentado) {
        this.diagnosticopresentado = diagnosticopresentado;
    }
}
